public class Persona {
    private String nombre;
    private String dni;
    private int edad;
    private double estatura;

    public Persona(String nombre, String dni, int edad, double estatura) {
        this.nombre = nombre;
        this.dni = dni;
        this.edad = edad;
        this.estatura = estatura;
    }
}
